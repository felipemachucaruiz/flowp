type Language = keyof typeof translations;

interface I18nContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: TranslationKey) => string;
  formatDate: (date: Date | string, options?: Intl.DateTimeFormatOptions) => string;
  formatDateTime: (date: Date | string) => string;
}

// Map languages to locales
const localeMap: Record<Language, string> = {
  en: "en-US",
  es: "es-ES",
  pt: "pt-BR",
};

const I18nContext = createContext<I18nContextType | null>(null);

// Detect browser language and map to supported languages
function detectBrowserLanguage(): Language {
  const browserLang = navigator.language || (navigator as any).userLanguage || "en";
  const langCode = browserLang.split("-")[0].toLowerCase();
  
  // Map browser language codes to our supported languages
  if (langCode === "es") return "es";
  if (langCode === "pt") return "pt";
  return "en"; // Default to English
}

// Get initial language from localStorage, browser, or fallback
function getInitialLanguage(initialLanguage?: string): Language {
  try {
    // First check localStorage for saved preference
    const savedLang = localStorage.getItem("flowp_language");
    if (savedLang && savedLang in translations) {
      return savedLang as Language;
    }
  } catch {
    // localStorage might not be available
  }
  
  // Then check if initialLanguage was provided and is valid
  if (initialLanguage && initialLanguage in translations) {
    return initialLanguage as Language;
  }
  
  // Finally, detect from browser
  try {
    return detectBrowserLanguage();
  } catch {
    return "en";
  }
}

export function I18nProvider({ children, initialLanguage }: { children: ReactNode; initialLanguage?: string }) {
  const [language, setLanguageState] = useState<Language>(() => {
    try {
      return getInitialLanguage(initialLanguage);
    } catch {
      return "en";
    }
  });

  // Update language when initialLanguage prop changes (e.g., after tenant settings update)
  useEffect(() => {
    if (initialLanguage && initialLanguage in translations && initialLanguage !== language) {
      setLanguageState(initialLanguage as Language);
    }
  }, [initialLanguage]);

  useEffect(() => {
    // Save language preference to localStorage whenever it changes
    try {
      localStorage.setItem("flowp_language", language);
    } catch {
      // localStorage might not be available
    }
  }, [language]);

  const setLanguage = (lang: Language) => {
    if (lang in translations) {
      setLanguageState(lang);
      try {
        localStorage.setItem("flowp_language", lang);
      } catch {
        // localStorage might not be available
      }
    }
  };

  const t = (key: TranslationKey): string => {
    try {
      const translation = translations[language]?.[key];
      if (!translation) {
        return translations.en[key] || String(key);
      }
      return translation;
    } catch {
      return String(key);
    }
  };

  const formatDate = (date: Date | string, options?: Intl.DateTimeFormatOptions): string => {
    const dateObj = typeof date === "string" ? new Date(date) : date;
    const locale = localeMap[language];
    const defaultOptions: Intl.DateTimeFormatOptions = {
      year: "numeric",
      month: "short",
      day: "numeric",
    };
    return new Intl.DateTimeFormat(locale, options || defaultOptions).format(dateObj);
  };

  const formatDateTime = (date: Date | string): string => {
    const dateObj = typeof date === "string" ? new Date(date) : date;
    const locale = localeMap[language];
    return new Intl.DateTimeFormat(locale, {
      year: "numeric",
      month: "short",
      day: "numeric",
      hour: "2-digit",
      minute: "2-digit",
    }).format(dateObj);
  };

  return (
    <I18nContext.Provider value={{ language, setLanguage, t, formatDate, formatDateTime }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within an I18nProvider");
  }
  return context;
}

